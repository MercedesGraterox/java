package sample;

public class Pasajero {
  int nroviaje;
  int cantpasajes;
  int DNI;
  int preciounitario;
  String nombrepasajero;
  String combodestino;

  public int getNroviaje() {
    return nroviaje;
  }

  public void setNroviaje(int nroviaje) {
    this.nroviaje = nroviaje;
  }

  public int getCantpasajes() {
    return cantpasajes;
  }

  public void setCantpasajes(int cantpasajes) {
    this.cantpasajes = cantpasajes;
  }

  public int getDNI() {
    return DNI;
  }

  public void setDNI(int DNI) {
    this.DNI = DNI;
  }

  public int getPreciounitario() {
    return preciounitario;
  }

  public void setPreciounitario(int preciounitario) {
    this.preciounitario = preciounitario;
  }

  public String getNombrepasajero() {
    return nombrepasajero;
  }

  public void setNombrepasajero(String nombrepasajero) {
    this.nombrepasajero = nombrepasajero;
  }

  public String getCombodestino() {
    return combodestino;
  }

  public void setCombodestino(String combodestino) {
    this.combodestino = combodestino;
  }





}
